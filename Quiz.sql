use quiz;
CREATE TABLE user (
    userID int NOT NULL AUTO_INCREMENT,
    userName VARCHAR(255),
    score INT
);


CREATE TABLE Quiz (
    QuizID INT PRIMARY KEY,
    difficultyLevel VARCHAR(255),
    score INT
);

